tasks = ["A", "B", "C", "D", "E", "F"]

print("tasks[1:4]:", tasks[1:4])    # 인덱스 1,2,3
print("tasks[:3]:",  tasks[:3])     # 처음부터 인덱스 0,1,2
print("tasks[3:]:",  tasks[3:])     # 인덱스 3부터 끝까지
print("tasks[-2:]:", tasks[-2:])    # 뒤에서 두 개
print("tasks[::2]:", tasks[::2])    # 0,2,4번 인덱스
print("tasks[::-1]:",tasks[::-1])   # 전체 역순